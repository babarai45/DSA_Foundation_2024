# what  recursion tree method is and how to use it to solve the time complexity of recursive algorithms?
# 1. what is recursion tree method?
# a method to solve the time complexity of recursive algorithms by drawing a recursion tree. 
# in the recursion tree, each node represents the cost of a single sub-problem in the recursive algorithm.
# the total cost of the algorithm is the sum of the costs of all the nodes in the recursion tree. 
# and the time complexity of the algorithm is the number of nodes in the recursion tree.
# it is a visual way to understand the time complexity of recursive algorithms.
